<script lang="ts">
  import { browser } from '$app/environment';
  import { modalStore, type ModalSettings } from '@skeletonlabs/skeleton';
  import magnify from '$lib/assets/magnify.svg?raw';

  export let cls = '';
  export let width = '';

  function triggerSearch(): void {
    const d: ModalSettings = {
      type: 'component',
      component: 'search',
      position: 'item-start'
    };
    modalStore.trigger(d);
  }

  let isOsMac = false;
  if (browser) {
    let os = navigator.userAgent;
    isOsMac = os.search('Mac') !== -1;
  }
</script>

<!-- Search -->
<div class={cls}>
  <button
    class="btn p-2 px-4 space-x-4 variant-soft hover:variant-soft-primary {width}"
    on:click={triggerSearch}>
    <span class="w-6">{@html magnify}</span>
    <span class="inline-block badge variant-soft"
      >{isOsMac ? 'âŒ˜' : 'Ctrl'}+K</span>
  </button>
</div>

